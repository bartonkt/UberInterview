- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}


// Customize the number of rows in the table view.
- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [self.generalArray count];
}


// Customize the appearance of table view cells.
- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier] autorelease];
    }
    
    // Set up the cell...
	NSUInteger row = [indexPath row];
	cell.textLabel.text = [generalArray objectAtIndex:row];
	
    return cell;
}


- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    // Navigation logic may go here. Create and push another view controller.
	// AnotherViewController *anotherViewController = [[AnotherViewController alloc] initWithNibName:@"AnotherView" bundle:nil];
	// [self.navigationController pushViewController:anotherViewController];
	// [anotherViewController release];
	
	NSInteger row = [indexPath row];
	if (self.generalDetailViewController == nil) {
		GeneralDetailViewController *aGeneralDetail = [[GeneralDetailViewController alloc] initWithNibName:@"GeneralDetailView" bundle:nil];
		self.generalDetailViewController = aGeneralDetail;
		[aGeneralDetail release];
	}
	generalDetailViewController.title = [NSString stringWithFormat:@"%@", [generalArray objectAtIndex:row]];
	NSInteger row_value = row;
	generalDetailViewController.row_value = row_value;
	
	[self.navigationController pushViewController:generalDetailViewController animated:YES];
}


